/**
 * Some hosts (e.g. Render) default to `node dist/main.js`.
 * Nest emits `dist/src/main.js` — this shim forwards for compatibility.
 */
const fs = require('fs');
const path = require('path');

const dist = path.join(__dirname, '..', 'dist');
const shim = path.join(dist, 'main.js');
const target = './src/main.js';

if (!fs.existsSync(path.join(dist, 'src', 'main.js'))) {
  console.warn(
    '[write-dist-main-shim] dist/src/main.js missing; skip shim (run nest build first).',
  );
  process.exit(0);
}

fs.writeFileSync(shim, `// Auto-generated by scripts/write-dist-main-shim.cjs\nrequire('${target}');\n`);
console.log('[write-dist-main-shim] wrote', shim);
